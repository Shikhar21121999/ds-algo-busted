int k=0;

int recur(int strt){
	k++;
	if(k>20)return 0;
	// declare pointers
	int last=strt+line_width;
	if(last>=n)last=n-1;
	int trav_ind=last;

	// base case
	if(strt>n)return 0;
	int min_cost=IINF;
	/*cout<<strt<<space<<last<<space<<trav_ind<<nextline;
	cout<<inp[trav_ind]<<nextline;*/
	while(trav_ind>strt){
		if(inp[trav_ind]==' '){
			int curr_cost=last-trav_ind+recur(trav_ind+1);
			min_self(min_cost,curr_cost);
		}
		trav_ind--;
	}
	return min_cost;

}

